package com.otma.entities;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.Table;
@Entity
@Table(name="RESOURCE")
public class Resource implements Serializable {
	@Id
	@Column(name="RESOURCE_NO")
	@GeneratedValue(strategy=GenerationType.IDENTITY)
	protected int resourceNo;
	@Column(name="RESOURCE_NAME")
	protected String resourceName;
	protected int experience;
	protected String skills;
	protected String email;
	
	@ManyToOne
	@JoinColumn(name="PROJECT_NO")
	protected Project project;

	public int getResourceNo() {
		return resourceNo;
	}

	public void setResourceNo(int resourceNo) {
		this.resourceNo = resourceNo;
	}

	public String getResourceName() {
		return resourceName;
	}

	public void setResourceName(String resourceName) {
		this.resourceName = resourceName;
	}

	public int getExperience() {
		return experience;
	}

	public void setExperience(int experience) {
		this.experience = experience;
	}

	public String getSkills() {
		return skills;
	}

	public void setSkills(String skills) {
		this.skills = skills;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public Project getProject() {
		return project;
	}

	public void setProject(Project project) {
		this.project = project;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((email == null) ? 0 : email.hashCode());
		result = prime * result + experience;
		result = prime * result + ((resourceName == null) ? 0 : resourceName.hashCode());
		result = prime * result + resourceNo;
		result = prime * result + ((skills == null) ? 0 : skills.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Resource other = (Resource) obj;
		if (email == null) {
			if (other.email != null)
				return false;
		} else if (!email.equals(other.email))
			return false;
		if (experience != other.experience)
			return false;
		if (resourceName == null) {
			if (other.resourceName != null)
				return false;
		} else if (!resourceName.equals(other.resourceName))
			return false;
		if (resourceNo != other.resourceNo)
			return false;
		if (skills == null) {
			if (other.skills != null)
				return false;
		} else if (!skills.equals(other.skills))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "Resource [resourceNo=" + resourceNo + ", resourceName=" + resourceName + ", experience=" + experience
				+ ", skills=" + skills + ", email=" + email + ", project=" + project + "]";
	}
	
	
}
